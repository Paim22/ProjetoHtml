    <h2>Resumo do Livro "Auto da Barca do Inferno"</h2>
    <p>
        "Auto da Barca do Inferno" é uma obra teatral escrita por Gil Vicente, encenada pela primeira vez em 1517.
        A peça é uma sátira social que retrata a viagem das almas humanas após a morte, sendo julgadas e direcionadas pela
        figura do Diabo (representando as tentações e os pecados) ou pelo Anjo (simbolizando a redenção).
        Com diálogos satíricos e elementos cômicos, a obra critica as normas morais e sociais da época,
        convidando à reflexão sobre as escolhas e a conduta humana.
