    <h2>Resumo do Livro "Morte e Vida Severina"</h2>
    <p id="resumo4">
        "Morte e Vida Severina" é uma obra de João Cabral de Melo Neto, publicada em 1955. Trata-se de um auto em forma de poema
        que narra a jornada de Severino, um retirante que atravessa o sertão nordestino em busca de uma vida melhor. O auto aborda
        as dificuldades enfrentadas pelos trabalhadores rurais, a seca, a fome e a esperança por uma existência mais digna.
        A linguagem concisa e objetiva de João Cabral de Melo Neto ressalta a dura realidade do povo nordestino, evidenciando as
        injustiças sociais e as adversidades enfrentadas pelos mais desfavorecidos.
