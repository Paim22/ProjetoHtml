    <p id="resumo7">
    "Triste Fim de Policarpo Quaresma" é um romance do escritor brasileiro Lima Barreto, publicado em 1911. A obra narra a história de Policarpo Quaresma, um nacionalista patriótico e ingênuo que sonha em promover reformas e valorizar a cultura brasileira. 
    Policarpo é um funcionário público singular que acredita no potencial do Brasil e defende mudanças radicais para fortalecer a identidade nacional. Ele se dedica a diversos projetos de patriotismo, como o estudo do tupi-guarani, o plantio de árvores nacionais e a valorização da cultura nacional, mas é ridicularizado e marginalizado pelas autoridades e pela sociedade.
    A narrativa explora temas como nacionalismo, patriotismo, burocracia, corrupção, e a relação entre ideais e realidade. A trajetória de Policarpo ilustra as contradições e dificuldades enfrentadas por aqueles que buscam transformações sociais e culturais em um contexto adverso.
