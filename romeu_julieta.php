    <h2>Resumo do Livro "O Cortiço"</h2>
    <p>
    "Romeu e Julieta" é uma das obras mais conhecidas e aclamadas de Shakespeare, representando uma das maiores histórias de amor trágico da literatura. A peça conta a história de dois jovens amantes, Romeu Montecchio e Julieta Capuleto, cujas famílias estão em conflito.
    No centro da trama está o romance proibido entre Romeu e Julieta, que enfrentam obstáculos significativos devido ao ódio entre suas famílias. O casal secreta-se amorosamente e, apesar dos desafios, decide se casar em segredo. No entanto, um mal-entendido leva a consequências trágicas, culminando em um desfecho devastador.
    A peça aborda temas como o poder do destino, o conflito entre o amor e o ódio, a juventude e a impulsividade. A paixão intensa e a tragédia de "Romeu e Julieta" continuam a cativar o público até os dias de hoje, tornando-a atemporal e uma das obras mais reverenciadas da literatura mundial.
