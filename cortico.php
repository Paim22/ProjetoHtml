    <h2>Resumo do Livro "O Cortiço"</h2>
    <p id="resumo3">
        "O Cortiço" é um romance naturalista escrito por Aluísio Azevedo e publicado em 1890. A obra é uma crítica social
        que retrata a vida em um cortiço na cidade do Rio de Janeiro, evidenciando os conflitos de classes, comportamentos
        humanos e questões raciais da época.
        Através da narrativa, Aluísio Azevedo apresenta personagens marcantes que lutam por sobrevivência, amor e status social
        em um ambiente urbano precário. O livro aborda temas como miséria, sensualidade, corrupção e a luta pela ascensão social,
        refletindo as condições e tensões da sociedade oitocentista.
