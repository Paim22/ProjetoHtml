    <h2>Resumo do Livro "O Cortiço"</h2>
    <p>
    "O Quinze" é um romance regionalista que retrata a seca no sertão nordestino do Brasil, especificamente no estado do Ceará, durante a chamada "tragédia das secas" de 1915. A obra destaca as duras condições enfrentadas pelo povo sertanejo e as consequências sociais, econômicas e emocionais da seca.
    No livro, Rachel de Queiroz narra as histórias de Conceição, uma jovem mulher de classe média que enfrenta as mudanças em sua vida devido à seca, e de Chico Bento, um vaqueiro que lida com os desafios da sobrevivência em um cenário tão árido.
    A narrativa repleta de realismo e sensibilidade descreve a luta pela sobrevivência, a solidariedade entre as comunidades e as relações humanas em meio à adversidade, mostrando as diferentes perspectivas e a força do povo nordestino diante de uma das maiores tragédias naturais da região.
